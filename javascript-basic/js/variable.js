//変数の宣言
let userName;

//値の代入
userName='侍太郎';

//変数の宣言・値の代入
let userNumber=55; //変数宣言をするとき代入する値が数値である場合はlet userNumber= 55;というように変数名と値を続けて記述する。

//コンソールへの出力
console.log(userName);
console.log(userNumber);

//値の再代入
userName= '侍花子';
userNumber= 88;

//コンソールへの出力
console.log(userName);
console.log(userNumber);